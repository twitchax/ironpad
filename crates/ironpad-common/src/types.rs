//! Shared types for the ironpad compilation pipeline, notebook persistence, and UI.

use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use uuid::Uuid;

// ── Compilation Types ────────────────────────────────────────────────────────

/// Request sent from the client to compile a single cell.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CompileRequest {
    pub notebook_id: String,
    pub cell_id: String,
    pub source: String,
    pub cargo_toml: String,
    /// Type tags from all previous cells, used to generate typed input bindings.
    #[serde(default)]
    pub previous_cell_types: Vec<Option<String>>,
    /// Notebook-level shared Cargo.toml whose dependencies are merged with per-cell deps.
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub shared_cargo_toml: Option<String>,
}

/// Response from the server after a compilation attempt.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CompileResponse {
    /// The compiled WASM blob (empty on failure).
    pub wasm_blob: Vec<u8>,
    /// Compiler diagnostics (errors, warnings, notes).
    pub diagnostics: Vec<Diagnostic>,
    /// Whether the result was served from cache.
    pub cached: bool,
    /// Number of scaffold preamble lines for diagnostic line-number adjustment.
    #[serde(default)]
    pub preamble_lines: u32,
    /// JS glue module generated by `wasm-bindgen` (ES module source).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub js_glue: Option<String>,
}

/// A single compiler diagnostic (error, warning, or note).
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct Diagnostic {
    pub message: String,
    pub severity: Severity,
    pub spans: Vec<Span>,
    /// Optional Rust error code (e.g. `"E0308"`), used for linking to the
    /// [Rust error index](https://doc.rust-lang.org/error_codes/).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub code: Option<String>,
}

/// Severity level for a compiler diagnostic.
#[derive(Clone, Debug, Serialize, Deserialize, PartialEq, Eq)]
pub enum Severity {
    Error,
    Warning,
    Note,
}

/// A source-code span associated with a diagnostic.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct Span {
    pub line_start: u32,
    pub line_end: u32,
    pub col_start: u32,
    pub col_end: u32,
    pub label: Option<String>,
}

// ── Execution Result Types ───────────────────────────────────────────────────

/// Result of executing a compiled WASM cell.
///
/// Populated after the WASM executor runs `cell_main`.  Until execution is
/// wired up (T-036/T-037), the UI can still render this type when it becomes
/// available via the `CellOutputPanel`.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct ExecutionResult {
    /// JSON-serialized `Vec<DisplayPanel>` from the cell's output panels.
    /// Parse with `serde_json::from_str` on the frontend. Falls back to a single
    /// Text panel if parsing fails (backward compatibility).
    pub display_text: Option<String>,
    /// Raw output bytes (bincode-serialized payload for the next cell).
    pub output_bytes: Vec<u8>,
    /// Wall-clock execution time in milliseconds.
    pub execution_time_ms: f64,
    /// Rust type tag of the cell output (e.g. `"u32"`, `"String"`, `"Vec<i32>"`).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub type_tag: Option<String>,
}

// ── Cell Type ────────────────────────────────────────────────────────────────

/// Distinguishes code cells (compiled to WASM) from markdown documentation cells.
#[derive(Clone, Debug, Serialize, Deserialize, Default, PartialEq)]
pub enum CellType {
    #[default]
    Code,
    Markdown,
}

// ── Notebook Persistence Types ───────────────────────────────────────────────

/// Full notebook manifest, serialized as `ironpad.json` on disk.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct NotebookManifest {
    pub id: Uuid,
    pub title: String,
    pub created_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
    pub compiler_version: String,
    pub cells: Vec<CellManifest>,
    /// Notebook-level shared Cargo.toml content (merged with per-cell deps at scaffold time).
    #[serde(default, skip_serializing_if = "Option::is_none")]
    pub shared_cargo_toml: Option<String>,
}

/// A single cell entry within a notebook manifest.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CellManifest {
    pub id: String,
    pub order: u32,
    pub label: String,
    /// Cell type — `Code` (default) or `Markdown`. Missing field deserializes as `Code`
    /// for backward compatibility with existing notebooks.
    #[serde(default)]
    pub cell_type: CellType,
}

/// The source code and Cargo.toml content of a cell.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct CellContent {
    pub source: String,
    pub cargo_toml: String,
}

/// Lightweight summary used for the notebook list view.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct NotebookSummary {
    pub id: Uuid,
    pub title: String,
    pub updated_at: DateTime<Utc>,
    pub cell_count: usize,
}
